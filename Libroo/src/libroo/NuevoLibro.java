
package libroo;


public class NuevoLibro {
    
    public int ISBN;
    
    public String titulo;
    
    public String autor;
    
    public int paginas;

    public NuevoLibro() {
    }
    
    

    public NuevoLibro(int ISBN, String titulo, String autor, int paginas) {
        this.ISBN = ISBN;
        this.titulo = titulo;
        this.autor = autor;
        this.paginas = paginas;
    }
    
    
    
}
